package org.hallowee;

public class Fiesta {
    //ATRIBUTOS
    public String fecha;
    public double costosToital;
    public double gastosTotal;
    public float ganancias;
    public int numeroEntradas;
    public double valorEntrada;
    public double alimentos;
    public double bebidas;
    public double costosLugar;
    public double costosEquipo;


    //CONSTRUCTOR

    public Fiesta() {

    }

    public Fiesta(String fecha, double costosToital, double gastosTotal, float ganancias, int numeroEntradas, double valorEntrada) {
        this.fecha = fecha;
        this.costosToital = costosToital;
        this.gastosTotal = gastosTotal;
        this.ganancias = ganancias;
        this.numeroEntradas = numeroEntradas;
        this.valorEntrada = valorEntrada;
    }
    //METODOS


}
